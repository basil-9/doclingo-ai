-- DocLingo AI V25 database foundation
-- Run this entire file once in Supabase SQL Editor.

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text,
  full_name text,
  avatar_url text,
  credits integer not null default 5 check (credits >= 0),
  last_reward_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.credit_transactions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  amount integer not null,
  transaction_type text not null check (transaction_type in ('signup','tool_use','ad_reward','purchase','admin')),
  description text,
  created_at timestamptz not null default now()
);

create table if not exists public.tool_usage (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  tool_id text not null,
  credits_spent integer not null default 2,
  created_at timestamptz not null default now()
);

create table if not exists public.ai_results (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  tool_id text not null,
  title text not null,
  result_text text not null,
  pages_processed integer not null default 0,
  created_at timestamptz not null default now()
);

alter table public.profiles enable row level security;
alter table public.credit_transactions enable row level security;
alter table public.tool_usage enable row level security;
alter table public.ai_results enable row level security;

grant select on public.profiles to authenticated;
grant select on public.credit_transactions to authenticated;
grant select, insert on public.tool_usage to authenticated;
grant select, insert, delete on public.ai_results to authenticated;
grant usage, select on all sequences in schema public to authenticated;

drop policy if exists "Users read own profile" on public.profiles;
create policy "Users read own profile" on public.profiles for select to authenticated using ((select auth.uid()) = id);

drop policy if exists "Users read own transactions" on public.credit_transactions;
create policy "Users read own transactions" on public.credit_transactions for select to authenticated using ((select auth.uid()) = user_id);

drop policy if exists "Users read own usage" on public.tool_usage;
create policy "Users read own usage" on public.tool_usage for select to authenticated using ((select auth.uid()) = user_id);
drop policy if exists "Users insert own usage" on public.tool_usage;
create policy "Users insert own usage" on public.tool_usage for insert to authenticated with check ((select auth.uid()) = user_id);

drop policy if exists "Users read own AI results" on public.ai_results;
create policy "Users read own AI results" on public.ai_results for select to authenticated using ((select auth.uid()) = user_id);
drop policy if exists "Users insert own AI results" on public.ai_results;
create policy "Users insert own AI results" on public.ai_results for insert to authenticated with check ((select auth.uid()) = user_id);
drop policy if exists "Users delete own AI results" on public.ai_results;
create policy "Users delete own AI results" on public.ai_results for delete to authenticated using ((select auth.uid()) = user_id);

create or replace function public.handle_new_user() returns trigger language plpgsql security definer set search_path = public as $$
begin
  insert into public.profiles(id,email,full_name,avatar_url,credits)
  values(new.id,new.email,coalesce(new.raw_user_meta_data->>'full_name',split_part(new.email,'@',1)),new.raw_user_meta_data->>'avatar_url',5)
  on conflict (id) do nothing;
  insert into public.credit_transactions(user_id,amount,transaction_type,description) values(new.id,5,'signup','Welcome credits');
  return new;
end;$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created after insert on auth.users for each row execute procedure public.handle_new_user();

