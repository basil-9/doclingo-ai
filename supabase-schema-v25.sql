create or replace function public.spend_credits(credit_amount integer) returns integer language plpgsql security definer set search_path = public as $$
declare balance integer; uid uuid := auth.uid();
begin
  if uid is null then raise exception 'Sign in required'; end if;
  if credit_amount <= 0 or credit_amount > 50 then raise exception 'Invalid credit amount'; end if;
  update profiles set credits=credits-credit_amount,updated_at=now() where id=uid and credits>=credit_amount returning credits into balance;
  if balance is null then raise exception 'Not enough credits'; end if;
  insert into credit_transactions(user_id,amount,transaction_type,description) values(uid,-credit_amount,'tool_use','Document tool usage');
  return balance;
end;$$;

create or replace function public.claim_demo_reward(reward_amount integer default 2) returns integer language plpgsql security definer set search_path = public as $$
declare balance integer; uid uuid := auth.uid(); last_claim timestamptz;
begin
  if uid is null then raise exception 'Sign in required'; end if;
  if reward_amount <> 2 then raise exception 'Invalid reward amount'; end if;
  select last_reward_at into last_claim from profiles where id=uid for update;
  if last_claim is not null and last_claim > now()-interval '30 seconds' then raise exception 'Please wait before claiming another reward'; end if;
  update profiles set credits=credits+reward_amount,last_reward_at=now(),updated_at=now() where id=uid returning credits into balance;
  insert into credit_transactions(user_id,amount,transaction_type,description) values(uid,reward_amount,'ad_reward','Demo rewarded-ad credit');
  return balance;
end;$$;

revoke all on function public.spend_credits(integer) from public;
revoke all on function public.claim_demo_reward(integer) from public;
grant execute on function public.spend_credits(integer) to authenticated;
grant execute on function public.claim_demo_reward(integer) to authenticated;
